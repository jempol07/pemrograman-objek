    private void btn_enterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_enterActionPerformed
        
        try {                                          
            String query2 = "select * from kepala_sekolah WHERE Nama_Kepsek = (?)";
            PreparedStatement statement2 = SQL_SI.getConnection().prepareStatement(query2);
            statement2.setString(1, (String) cb_kepsek.getSelectedItem());
            ResultSet rs = statement2.executeQuery();

            Kepala_Sekolah kepsek = new Kepala_Sekolah();

            if (rs.next()){
                kepsek.setId_Kepsek(rs.getInt("Id_Kepsek"));
                kepsek.setNama_Kepsek(rs.getString("Nama_Kepsek"));
            }
            statement2.execute();
            statement2.close();
            
            String query = "UPDATE sekolah SET Id_Kepsek = (?) WHERE Nama_Sekolah = (?)";
            PreparedStatement statement = SQL_SI.getConnection().prepareStatement(query);
            statement.setInt(1, kepsek.getId_Kepsek());
            statement.setString(2, (String) cb_sekolah.getSelectedItem());
            
            statement.execute();
            statement.close();
            
            DefaultTableModel modelTable = (DefaultTableModel) tbl_transaksi.getModel();

            if ((cb_sekolah.getSelectedItem() == "pilih sekolah") && (cb_kepsek.getSelectedItem() == "pilih kepala sekolah")) 
                {
                    JOptionPane.showMessageDialog(null, "input data");
                }
            else   
            {
                query = "select sekolah.Nama_Sekolah, kepala_sekolah.Nama_Kepsek from kepala_sekolah, sekolah "
                        + "where sekolah.Id_Kepsek = kepala_sekolah.Id_Kepsek AND sekolah.Id_Kepsek != 0";
                 statement = SQL_SI.getConnection().prepareStatement(query);
                 rs = statement.executeQuery();

                 for (int i = modelTable.getRowCount() - 1; i > -1; i--) 
                    {
                        modelTable.removeRow(i);
                    }
                 
                 while(rs.next())
                 {
                     Object[] view = new Object[2];
                     view[0] = rs.getString("Nama_Sekolah");
                     view[1] = rs.getString("Nama_Kepsek");
                     modelTable.addRow(view);
                 }

                 statement.close();
                 rs.close();
                 tbl_transaksi.setModel(modelTable);
            }
       
            cb_sekolah.setSelectedIndex(0);
            cb_kepsek.setSelectedIndex(0);
        } catch (Exception ex) {
            Logger.getLogger(TransaksiSekolahKepsek.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btn_enterActionPerformed

