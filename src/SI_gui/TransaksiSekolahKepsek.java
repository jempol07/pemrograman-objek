    private void tampil_combo() {
    try{
        koneksi();
        String query = "select Nama_Sekolah from sekolah";
        Statement st = con.createStatement();
        ResultSet rs = st.executeQuery(query);
        
        cb_sekolah.addItem("pilih sekolah");
        cb_kepsek.addItem("pilih kepala sekolah");
        
        while (rs.next()){
            String temp = rs.getString("Nama_Sekolah");
            cb_sekolah.addItem(temp);
            skulAll.add(temp);
        }
        
        query = "select Nama_Kepsek from Kepala_Sekolah where Nama_Kepsek != 'default'";
        rs = st.executeQuery(query);
        while (rs.next()){
            String temp = rs.getString("Nama_Kepsek");
            cb_kepsek.addItem(temp);
            kepAll.add(temp);
        }
        
        
    }catch (Exception ex) {
            Logger.getLogger(TransaksiSekolahKepsek.class.getName()).log(Level.SEVERE, null, ex);
            }
    }
    
